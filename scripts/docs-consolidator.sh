### **Installation**

```bash
# Clone the repository
git clone https://github.com/your-org/rensto.git
cd rensto

# Install dependencies
npm install

# Set up environment variables
cp .env.example .env
# Edit .env with your configuration

# Run database migrations
npm run db:migrate

# Start the development server
npm run dev
```

### **Customer Portal Generation**

```javascript
const { CustomerPortalGenerator } = require('./customer-portal-generator.js');

const generator = new CustomerPortalGenerator();

const customer = {
  name: "Acme Corporation",
  businessType: "SaaS Platform",
  planTier: "Enterprise",
  monthlyRevenue: "25,000",
  leadMetricName: "Active Users",
  leadMetricValue: "5,234",
  successRate: "97",
  totalRuns: "342"
};

const result = generator.generatePortal(customer, {
  businessType: 'saas',
  language: 'en',
  brandTheme: 'modern',
  includeAnalytics: true,
  includeMarketplace: true
});

console.log('Portal generated:', result.filename);
console.log('Deployed to:', result.url);
```

## 📊 API Reference

### **Customer Portal API**

#### **Generate Portal**
```http
POST /api/portals/generate
Content-Type: application/json

{
  "customer": {
    "name": "Acme Corp",
    "businessType": "saas",
    "planTier": "enterprise"
  },
  "options": {
    "language": "en",
    "brandTheme": "modern",
    "includeAnalytics": true,
    "includeMarketplace": true
  }
}
```

#### **Update Portal**
```http
PUT /api/portals/:id
Content-Type: application/json

{
  "customer": {
    "monthlyRevenue": "30,000",
    "successRate": "98"
  }
}
```

### **Agent Management API**

#### **List Agents**
```http
GET /api/agents
Authorization: Bearer <token>
```

#### **Create Agent**
```http
POST /api/agents
Content-Type: application/json

{
  "name": "Lead Qualification Agent",
  "type": "lead_processing",
  "config": {
    "workflowId": "workflow_123",
    "triggers": ["new_lead"],
    "settings": {
      "minScore": 70,
      "autoFollowUp": true
    }
  }
}
```

